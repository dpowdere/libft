#include <stddef.h>

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*m1;
	unsigned char	*m2;

	i = 0;
	m1 = (unsigned char *)s1;
	m2 = (unsigned char *)s2;
	while (i < n && m1[i] && m2[i] && m1[i] == m2[i])
		++i;
	--i;
	return ((int)(m1[i] - m2[i]));
}
